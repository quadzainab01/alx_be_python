#assign specific values to variables
number1 = 10
number2 = 5

#sum of the variables
sum = number1 + number2

print(f"Addition of {number1} and {number2} is {sum}")

#difference of the variables
difference = number1 - number2

print(f"Subtraction of {number1} from {number2} is {difference}")

#product of the variables
product = number1 * number2

print(f"Multiplication of {number1} and {number2} is {product}")

